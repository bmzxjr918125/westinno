package west.util;
/**
 * 增删该查操作状态，及返回提醒信息模型
 * @author  BianMingZhou
 *
 */
public class Notify {
	/**
	 * 操作状态码
	 * 0为当前误操作，不做任何提示
	 * 1为操作成功，给成功提示
	 * 2为操作不通过，给错误提示
	 * 3为操作异常
	 */
	private int code;
	/**
	 * 提示信息
	 */
	private String msg;
	public int getCode() {
		return code;
	}
	public void setCode(int code) {
		this.code = code;
	}
	public String getMsg() {
		return msg;
	}
	public void setMsg(String msg) {
		this.msg = msg;
	}
	
	public Notify() {}
	
	public Notify(int code, String msg) {
		super();
		this.code = code;
		this.msg = msg;
	}
	
}